def SUM(a, b):
    """Calcule la somme de deux nombres.

    Paramètres :
    a : premier nombre
    b : deuxième nombre

    Retourne :
    la somme de a et b
    """
    return a + b


def subtraction(a, b):
    """Calcule la différence entre deux nombres.

    Paramètres :
    a : premier nombre
    b : deuxième nombre

    Retourne :
    la différence entre a et b
    """
    return a - b
